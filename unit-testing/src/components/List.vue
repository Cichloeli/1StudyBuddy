<template>
  <div>
    <h1>My To Do List</h1>
    <input v-model="newItem" >
    <button @click="addItemToList()">Add</button>
    <!-- displays list -->
    <ul>
      <p>{{ this.listItems }}</p>
    </ul>
  </div>
</template>

<script>
export default {
  name: 'test',
  data () {
    return {
      listItems: ['buy food', 'play games', 'sleep'],
      newItem: ''
    }
  },
  methods: {
    addItemToList () {
      this.listItems.push(this.newItem)
      this.newItem = ''
    }
  }
}
</script>
